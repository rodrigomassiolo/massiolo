function setLanguage(lang, deleteBR) {
    if (deleteBR) {
        document.querySelectorAll("br.d-md-none").forEach(br => br.remove());
    }

    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.dataset.i18n;
        const mainTranslation = translations[lang][key];
        if (mainTranslation === undefined) return;

        let finalContent = mainTranslation;
        let i = 1;
        while (translations[lang][`${key}_${i}`] !== undefined) {
            finalContent += "<br>" + translations[lang][`${key}_${i}`];
            i++;
        }

        if (deleteBR) {
            document.documentElement.classList.add("is-printing");
            document.body.classList.add("is-printing");
            finalContent = finalContent.replaceAll(/<br\s+class\s*=\s*['"]d-md-none['"]\s*\/?>/gi, ' ');
            finalContent = finalContent.replaceAll(/(<br\s*\/?>\s*){2,}/gi, '<br>');
        } else {
            document.documentElement.classList.remove("is-printing");
            document.body.classList.remove("is-printing");
        }

        if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
            el.value = finalContent;
        } else {
            el.innerHTML = finalContent;
        }
    });

    document.querySelectorAll(".lang-btn").forEach(btn => {
        btn.classList.remove("active");
    });

    document.querySelectorAll(".btn_" + lang).forEach(btn => {
        btn.classList.add("active");
        btn.blur();
    });
    localStorage.setItem("language", lang);
    if (globalThis.updateCVDownloadTitle) globalThis.updateCVDownloadTitle();
}

document.addEventListener("DOMContentLoaded", () => {
    const urlParams = new URLSearchParams(globalThis.location.search);
    const langParam = urlParams.get('lang');
    const printParam = urlParams.get('print');

    if (langParam) {
        if (printParam === 'true') {
            document.documentElement.classList.remove('theme--dark');
            document.documentElement.classList.add('theme--light');
        }
        setLanguage(langParam, printParam === 'true');
    } else {
        const savedLang = localStorage.getItem("language") || "es";
        setLanguage(savedLang);
    }
});

if (typeof module !== 'undefined') {
    module.exports = { translations, setLanguage };
}

